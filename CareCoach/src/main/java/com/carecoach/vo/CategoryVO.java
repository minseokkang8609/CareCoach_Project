package com.carecoach.vo;




public class CategoryVO {

	private String name;
	private int write_at;
	private int id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getWrite_at() {
		return write_at;
	}
	public void setWrite_at(int write_at) {
		this.write_at = write_at;
	}
	
	
	
}
